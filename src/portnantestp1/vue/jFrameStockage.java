    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        // TODO add your handling code here:

        if(txtBoxCapaMax.getText().isEmpty() || txtBoxNum.getText().isEmpty())
            return;

        try
        {
            int capa = Integer.parseInt(txtBoxCapaMax.getText());
            int num = Integer.parseInt(txtBoxNum.getText());
            if(capa > 0 && num>0)
            {
                if(Ports.getZoneStockage(num)!=null)
                    return;

                if(checkBoxDangeureux.isSelected() && listTypeMatieres.getSelectedValues().length > 0)
                {
                    stockage = new ZoneStockageD();
                    getMatieresDangeureuse();
                }
                else
                    stockage = new ZoneStockage();

                stockage.setCapaMax(capa);
                stockage.setCapaDispo(capa);
                stockage.setNum(num);
                Ports.ajoutZoneStockage(stockage);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Veuillez saisir une capacité entière positive");
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

