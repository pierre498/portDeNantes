    // Remplissage du tableau des matiere dangeureuse à partir du cmbBox
    private void getMatieresDangeureuse()
    {
        for(int i=0 ; i< listTypeMatieres.getSelectedValues().length ; i++)
        {
            String unType = (String)listTypeMatieres.getSelectedValues()[i];
            ((ZoneStockageD)stockage).addTypeDangers(unType);
        }
    }

    private void majListMatieres()
    {
        List<Matiere> lst = Ports.getListMatieres();
        DefaultListModel dlm = new DefaultListModel();
        for(int i=0 ; i<lst.size() ; i++)
        {
            dlm.addElement(lst.get(i).getNom());
        }
        listTypeMatieres.setModel(dlm);
    }

