    private void btnEnrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrgActionPerformed
        if(txtMatiere.getText().length()>2 && Ports.getMatiere(txtMatiere.getText())==null)
        {
            Ports.ajoutMatiere(txtMatiere.getText());
            majMatieres();
            txtMatiere.setText(null);
        }
}//GEN-LAST:event_btnEnrgActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        Ports.supprimeMatiere((String) (jTableMatieres.getValueAt(jTableMatieres.getSelectedRow(), 0)));
        majMatieres();
    }//GEN-LAST:event_btnSupprimerActionPerformed

