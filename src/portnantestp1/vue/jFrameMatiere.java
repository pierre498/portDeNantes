    private void majMatieres()
    {
        List<Matiere> lst = Ports.getListMatieres();
        ((DefaultTableModel) jTableMatieres.getModel()).setRowCount(lst.size());
        
        /*for(int i=0 ; i<jTableMatieres.getRowCount() ; i++)
        {
            jTableMatieres.setValueAt(null, i, 0);
        }*/
        
        for(int i=0 ; i<lst.size() ; i++)
        {
            jTableMatieres.setValueAt(lst.get(i).getNom(), i, 0);
        }
    }


